using System;

namespace one {
    public class Moto {
        public void Arrancar() {
            Console.WriteLine("rrrrrr");
        }

        public void Arrancar(string a) {
            Console.WriteLine("rrrrrr " + a);
        }
        
        public void Arrancar(string a, string b) {
            Console.WriteLine("rrrrrr " + a + " " + b);
        }
    }
}